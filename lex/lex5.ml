let string_of_token t = 
  let sp = Printf.sprintf in 
  match t with 
  | Text s         -> sp "Text(%s)" s 
  | Hexadecimal i  -> sp "Hexadecimal(%i)" i 
  | Decimal i      -> sp "Decimal(%i)" i 
  | Less_than      -> "(<)"
  | Greater_than   -> "(>)"
  | Ampercent      -> "(&)"
  | Single_quote   -> "(')"
  | Double_quote   -> "(\")"
  | EOF            -> "(EOF)" 

